n1 = int(input("enter number"))
print(n1)

n2 = int(input("enter number"))
print(n2)

c = input("enter choice(+,-,*,/,%)")
print(c)

if c == "+":
   print( n1 + n2)
elif c == "-":
    print( n1 - n2)
elif c == "*":
   print( n1 * n2)
elif c == "/":
    print( n1 / n2)
elif c =="%":
    print(n1%n2)


